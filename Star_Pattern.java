package pawan;

public class Star_Pattern {
	
	public static void main(String[] args)
	{
		int i,j;	
		
		for (i=1;i<=6;i++)		
		{
			System.out.println(); 
		
			for (j=1;j<=i;j++)
			{
				System.out.print("*");
			}
	}}

}
